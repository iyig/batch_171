package d9string_loops;

public class WhileLoop01 {
    public static void main(String[] args) {



        // 3 ten 6 kadar tam sayıları console yazdıırını

        for (int i = 3; i <6 ; i++) {
            System.out.print(i + " ");

        }
        System.out.println();

int i = 3;
        while( i< 6){
            System.out.print(i + " ");
            i++;
        }
    }

}
